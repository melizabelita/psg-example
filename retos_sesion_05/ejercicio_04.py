# 4. Calcular el volumen de un cubo donde uno de sus lados mide 10 metros,
# utilizando la fórmula general del volumen de un cubo
# V = a^3

# Longitud de los lados del cubo en metros
lado = 10

# Calcular el volumen del cubo usando la fórmula de volumen V = a^3
volumen = lado ** 3

# $l resultado
print("El volumen del cubo cuyo lado es 10 metros es:")
print(volumen)
